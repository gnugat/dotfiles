#!/usr/bin/env bash
# File: /12-bash/config/shopt.sh
# ──────────────────────────────────────────────────────────────────────────────
# 👓 Bash shell options.
# ──────────────────────────────────────────────────────────────────────────────

## ─────────────────────────────────────────────────────────────────────────────
## 🏺 Appends to the history file, instead of overwriting it.
## ─────────────────────────────────────────────────────────────────────────────
shopt -s histappend

## ─────────────────────────────────────────────────────────────────────────────
## 🪟 Checks the window size after each command.
## If necesary, updates LINES and COLUMNS values.
## ─────────────────────────────────────────────────────────────────────────────
shopt -s checkwinsize

## ─────────────────────────────────────────────────────────────────────────────
## 🔎 If set, the pattern "**" used in a pathname expansion context will
## match all files and zero or more directories and subdirectories.
## ─────────────────────────────────────────────────────────────────────────────
#shopt -s globstar

## ─────────────────────────────────────────────────────────────────────────────
## 🏁 Autocompletes with sudo.
## ─────────────────────────────────────────────────────────────────────────────
complete -cf sudo

## ─────────────────────────────────────────────────────────────────────────────
## 💫 Fixes typos in the `cd` command.
## ─────────────────────────────────────────────────────────────────────────────
shopt -s cdspell # Fix cd typos

## ─────────────────────────────────────────────────────────────────────────────
## 🏺 Logs multi-line commands.
## ─────────────────────────────────────────────────────────────────────────────
shopt -s cmdhist

## ─────────────────────────────────────────────────────────────────────────────
## 🎭 Replaces aliases with their commands.
## ─────────────────────────────────────────────────────────────────────────────
shopt -s expand_aliases

## ─────────────────────────────────────────────────────────────────────────────
## 😑 Case insensitive.
## ─────────────────────────────────────────────────────────────────────────────
shopt -s nocaseglob # Case insensitive

## ─────────────────────────────────────────────────────────────────────────────
## 😈 Evil (emacs) keybindings.
## ─────────────────────────────────────────────────────────────────────────────
set -o emacs
